window.addEventListener('DOMContentLoaded', function () {
  // We can store values in VARIABLES

  // A string is any value between to quotes (either "", or '' work)
  var name = 'John Doe'

  // An integer is any Whole or Floating point number
  var age = 35
  var score = 89.93

  // A Boolean is either a `true` or `false` value without quotes
  var isAdult = true

  // We can display content in the browsers inspector by using:
  // console.log('CONTENT')
  console.log('This is from the console')

  // We can also log variables to the console
  // and display the value that they represent
  console.log(name)

  // We can also add multiple items in a console.log by seperating values with a comma
  console.log('Name:', name)
  console.log('Age:', age)
  console.log('Score:', score)
  console.log('Is Adult:', isAdult)
})
